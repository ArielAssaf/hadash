export function t(locale: string, key: keyof typeof translations['en']) {
    const l = (translations[locale as Locale] ? locale : defaultLocale) as Locale;
    return translations[l][key] || translations[defaultLocale][key];
}

export function getLocalizedPath(path: string, locale: string) {
    const base = import.meta.env.BASE_URL.replace(/\/$/, '');
    const localizedPath = locale === defaultLocale ? path : `/${locale}${path === '/' ? '' : path}`;
    return `${base}${localizedPath}`;
}
