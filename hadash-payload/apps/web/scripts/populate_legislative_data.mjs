async function updateLegislativeContent() {
    try {
        console.log('üîç Discovering Space ID...');
        const spaceRes = await cdnClient.get('cdn/spaces/me');
        const spaceId = spaceRes.data.space.id;

        console.log('üîç Checking for Legislative Story...');
        let legislativeStory = null;
        try {
            const storiesRes = await mapiClient.get(`spaces/${spaceId}/stories`, { text_search: 'legislative' });
            legislativeStory = storiesRes.data.stories.find(s => s.slug === 'legislative');
        } catch (e) { console.log('Story check failed/not found'); }

        const newBody = [
            {
                component: "legislative_list",
                title: "◊î◊ô◊©◊í◊ô◊ù ◊ï◊ó◊ï◊ß◊ô◊ù",
                badge: "◊¢◊©◊ô◊ô◊î ◊û◊ï◊õ◊ó◊™",
                categories: [
                    {
                        name: legislativeData.laws_passed.title,
                        icon: legislativeData.laws_passed.icon,
                        items: legislativeData.laws_passed.items.map(item => ({
                            title: item.title,
                            description: item.description
                        }))
                    },
                    {
                        name: legislativeData.blocking_legislation.title,
                        icon: legislativeData.blocking_legislation.icon,
                        items: legislativeData.blocking_legislation.items.map(item => ({
                            title: item.title,
                            description: item.description
                        }))
                    }
                ]
            }
        ];

        const contentPayload = {
            component: "page",
            body: newBody
        };

        if (legislativeStory) {
            console.log(`üîÑ Updating Legislative Story (${legislativeStory.id})...`);
            await mapiClient.put(`spaces/${spaceId}/stories/${legislativeStory.id}`, {
                story: { content: contentPayload },
                publish: 1,
                lang: 'he'
            });
        } else {
            console.log('‚ú® Creating New Legislative Story...');
            await mapiClient.post(`spaces/${spaceId}/stories`, {
                story: {
                    name: 'Legislative',
                    slug: 'legislative',
                    content: contentPayload
                },
                publish: 1
            });
        }

        console.log('‚úÖ Legislative content populated with FULL lists!');

    } catch (error) {
        console.error('‚ùå Update Failed:', error.response ? error.response.data : error);
        console.error(error);
    }
}

updateLegislativeContent();
